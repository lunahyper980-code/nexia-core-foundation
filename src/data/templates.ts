// Função para buscar template por ID
export const getTemplateById = (id: string): TemplateData | undefined => {
  return allTemplates.find(t => t.id === id);
};

// Função para filtrar templates por tipo
export const getTemplatesByType = (type: 'app' | 'site'): TemplateData[] => {
  return allTemplates.filter(t => t.type === type || t.type === 'both');
};
